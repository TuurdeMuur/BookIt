    <div class="level__2--wrapper">
    <p class="level__text">Het boek die zich groten deels in een virtuele Game wereld afspeelt, is voor elke game liefhebber een genot om te lezen. De wereld verwijst niet enkel naar bestaande games maar toont je ook een inkijk in de videogame cultuur als een geheel.</p>
    <img src="../assets/SVG/bolletjes.svg" alt="bolletjes" class="bolletjes">
    <p class="level__text">Dit wil natuurlijk niet zeggen dat het lezen van dit boek een goede videogame kennis vraagt. De alledaagse leesliefhebber zal dan misschien  niet  altijdde subtiele hints of verwijzingen doorhebben des al niet te min blijven de spanning, intrige en personages universele geliefde elementen.</p>
    <img src="../assets/SVG/bolletjes.svg" alt="bolletjes" class="bolletjes" >
    <p class="level__text">Een groot deel van het boek bestaat er ook uit dat de personages verscheidene easter eggs in de game moet zien te vinden. Easter eggs of te wel onverwachte of ongedocumenteerde features of verwijzingen die vaak in games voorkomen zijn dan ook in het boek zelf verstopt en een zorgen er voor dat aandacht speurders een reden hebben om het boek een paar keer opnieuw door te lezen.</p>
