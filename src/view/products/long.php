    <h2 class="level__title">Level 5</h2>
    <div class="level__5-wrapper">
   <p class="level__text two">Wie denkt dat Ready Player One een van de zovele science-fiction boeken is zonder veel inhoud of boodschap aan de lezer heeft het zeker mis. Het boek slaagt er in om de subtiele kritiek die het heeft op de samenleving ook in 2020 een 10 jaar na de eerste druk van het boek nog steeds relevant te zijn.  We kunnen zelf zeggen dat sommige aspecten die Cline aankaart in zijn boek gezien kunnen worden als een voorspelling van het heden.</p>
    <p class="level__text three">Tot slot kunnen we zeggen dat Ready Player One een boek is die een groot publiek aanspreekt. Mensen die houden van het vinden van geheime boodschappen en liefhebbers van alles die 80’s is tot hardcore gamers. Bovenal is dit een boek voor mensen die houden van een goede roman boordevol avontuur en goede personages. Kortom Ready player One is een boek die niet te leur stelt voor eender welke lezer.</p>
