  <article class="level level__4">
    <h2 class="level__title">Level 4</h2>
    <div class="level__4-wrapper">
    <p class="level__text text-4">Als we het over Ready Player one het boek hebben kunnen we het onderwerp van de film uit 2018 niet uit de weg gaan. Zoals elk boek die verfilmd werd is er ook bij dit boek een hele boel veranderd of weggelaten. Het feit dat je de film uit 2018 al gezien hebt,  zou  dus zeker geen obstakel vormen om het boek alsnog te lezen. Het boek biedt nog zoveel meer en versterkt de beleving van het kijken naar de film alleen maar.</p>
    <p class="level__text text-4">Je kunt het boek zijn als een veel uitgebreidere en gedetailleerde versie van de film. De verhaallijn die in grote lijnen wel hetzelfde is gebleven is gaat in het boek veel dieper in op het hoe en waarom en dit zorgt er dan weer voor dat je het gevoel hebt dat je een veel diepere connectie hebt met de personages die je volgt in het boek. Ook de popcultuur reverences zijn voor de film gemoderniseerd in tegenstelling tot die in het boek. Dit maakt het mogelijk om (indien je de film al eerder zag) opnieuw op zoek te gaan naar de easter eggs en verborgen boodschappen in het boek.</p>
    </div>
  </article>
  <article class="level level__5">
    <h2 class="level__title">Level 5</h2>
    <div class="level__5-wrapper">
   <p class="level__text two">Wie denkt dat Ready Player One een van de zovele science-fiction boeken is zonder veel inhoud of boodschap aan de lezer heeft het zeker mis. Het boek slaagt er in om de subtiele kritiek die het heeft op de samenleving ook in 2020 een 10 jaar na de eerste druk van het boek nog steeds relevant te zijn. We kunnen zelf zeggen dat sommige aspecten die Cline aankaart in zijn boek gezien kunnen worden als een voorspelling van het nu.</p>
    <p class="level__text three">Tot slot kunnen we zeggen dat Ready Player One een boek is die een groot publiek aanspreekt. Mensen die houden van het vinden van geheime boodschappen en liefhebbers van alles die 80’s is tot hardcore gamers. Bovenal is dit een boek voor mensen die houden van een goede roman boordevol avontuur en goede personages. Kortom Ready player One is een boek die niet te leur stelt voor eender welke lezer.
</p>
